package fr.uge.eiffelbikestore.convertisseur;

public class Convertisseur {
	
	public double EURToUSD(double value) {
		return value * 1.05;
		
	}
	
	public double EURToJPY(double value) {
		return value * 141.53;
		
	}
	
	public double EURToGBP(double value) {
		return value * 0.86;
		
	}
	
	public double EURToAUD(double value) {
		return value * 1.55;
		
	}
}
